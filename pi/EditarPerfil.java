    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Sessao sessao = Sessao.getInstancia();
        int id = sessao.getIdUsuario();
        
        Conexao verifica = new Conexao();
        verifica.abrirConexao();
        verifica.rs = null;
        
        Conexao atualiza = new Conexao();
        atualiza.abrirConexao();
        atualiza.rs = null;
        
        Conexao atualizaLabel = new Conexao();
        atualizaLabel.abrirConexao();
        atualizaLabel.rs = null;
        
        Pattern p = Pattern.compile("^[\\w-]+(\\.[\\w-]+)*@([\\w-]+\\.)+[a-zA-Z]{2,7}$");
        Matcher m = p.matcher(emailtext.getText());
        if(!m.find()){
            JOptionPane.showMessageDialog(null, "E-mail inv√°lido!");
            emailtext.requestFocus();
        }else{
        try {
           verifica.st = verifica.con.createStatement();
           PreparedStatement st = verifica.con.prepareStatement("SELECT * FROM users WHERE senha =?");
           st.setString(1, senhaatualtext.getText());
           verifica.rs = st.executeQuery();
               if(verifica.rs.next()){
               atualiza.st = atualiza.con.createStatement();
               PreparedStatement at = atualiza.con.prepareStatement("UPDATE users SET nome = ?, senha = ?, email = ? WHERE ID_usuario = ?");
               at.setString(1, nometext.getText());
               at.setString(2, novasenhatext.getText());
               at.setString(3, emailtext.getText());
               at.setInt(4, id);
               at.executeUpdate();
               JOptionPane.showMessageDialog(null, "Perfil Atualizado com Sucesso!");
               atualizaLabel.st = atualizaLabel.con.createStatement();
               PreparedStatement al = atualizaLabel.con.prepareStatement("SELECT * FROM users WHERE ID_usuario =?");
               al.setInt(1, id);
               atualizaLabel.rs = al.executeQuery();
               if(atualizaLabel.rs.next()){
               sessao.setLoginUsuario(atualizaLabel.rs.getString("nome"));
               this.dispose();
               }
               }else{
                   JOptionPane.showMessageDialog(null, "Senha Atual incorreta!");
                   senhaatualtext.requestFocus();
               }
        } catch (SQLException | HeadlessException e) {
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

